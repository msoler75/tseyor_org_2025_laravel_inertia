                        </span></span><span>¿Cómo puedo obtener e instalar el programa
                        Paltalk?</span></a>&nbsp;<small><a href="#_Toc301130338TOC">Arriba</a></small></h4>

            <p><span>Tienes video-tutoriales disponibles en la web: <a target="_blank"
                        href="http://www.tseyor.com/tutorial1.html">http://www.tseyor.com/tutorial1.html</a></span></p>

            <p><span>O puedes seguir estos pasos:</span></p>

            <p><span>1. Visitar este enlace: <span class="MsoHyperlink"><a
                            href="http://www.paltalk.com/">http://www.paltalk.com</a></span></span></p>

            <p><span>2. Una vez en la página, hay unas
                    pestañitas arriba que indican los servicios. Hay que pulsar donde dice <i>"DOWNLOAD"</i>,
                    y cuando se abra la siguiente página, pulsar en <i>“Download Paltalk Scene” o “Download for
                        Free”</i>. Una vez descargado el archivo, sólo hace falta
                    abrirlo (ejecutarlo) y empezará la instalación del software.</span></p>

            <p><span>3. En la instalación aparece otra ventanita
                    que dice <i>“LICENSE AGREEMENT”.</i> Hay que seleccionar <i>“I agree to the
                        terms…”</i>. Y pulsar el botón <i>“NEXT”, </i>2 veces. El programa se copiará
                    en el disco y sólo queda pulsar el botón <i>“FINISH”</i>. El programa ya está
                    instalado y se ejecutará inmediatamente.</span></p>

            <p><span>4. Aparece una ventana con 2 casillas: “<i>NickName”</i>
                    y <i>“Password”</i>. Hay que pulsar donde dice <i>“New User? Get nickname”</i>.</span></p>

            <p><span>5. Aparecerá una nueva ventana, donde nos
                    piden unos datos. Se introduce el <i>Nickname</i> (nombre que usaremos dentro
                    de paltalk), la contraseña (2 veces para estar seguros), y la dirección de
                    e-mail (para que si algún día olvidas la contraseña puedas recibirla en tu
                    correo). Si el nombre elegido no está disponible, o contiene caracteres no
                    aceptados, dará un mensaje de error y nos obligará a cambiarlo.</span></p>

            <p><span>6. También nos piden introducir una pregunta
                    de seguridad más, que es la pregunta secreta o <i>“Security Question”</i>, y
                    también introduciremos la respuesta a esa pregunta. Podemos elegir por ejemplo <i>“What
                        is your favorite Pet’s name?”</i>, que significa “¿Cuál es tu nombre de mascota
                    favorito?”, e introducimos la respuesta que deseemos y podamos recordar.</span></p>

            <p><span>7. Antes de pulsar el botón <i>“FINISH”</i>
                    es recomendable desactivar las casillas <i>“Get the free Ask Search…”</i> y <i>“I
                        agree the toolbar terms of use”</i>, o nos instalará una barra de utilidades en
                    el navegador, que es innecesaria y molesta.</span></p>

            <p><span>8. Si todo va bien, el programa arranca y
                    se conecta al servidor, y ya estamos dentro de Paltalk, donde puedes acceder a
                    todas las salas de conversación.</span></p>


            <h4><a name="_Toc301131286"></a><a name="_Toc301130343"></a><a name="_Toc194584903"><span>3.134<span
                            style="font:7.0pt 'Times New Roman'">&nbsp;
                        </span></span><span>¿Cómo puedo conversar en la sala?</span></a>&nbsp;<small><a
                        href="#_Toc301130338TOC">Arriba</a></small></h4>

            <p><span>Una vez dentro de la sala, en la parte
                    inferior de la ventana hay un espacio donde se puede escribir texto. Se puede
                    pulsar la tecla <i>Enter</i> para enviar el texto a la sala y que los demás lo
                    lean. Simplemente, escribe “hola” y pulsa <i>Enter</i>.</span></p>

            <p><span>La sala dispone también de sonido, y
                    podrás escuchar a través del audio del ordenador la voz (o la música) de quién
                    esté tomando en ese momento el micrófono. Sólo una persona a la vez puede tomar
                    el micrófono. Al lado del botón que dice “Mute”, hay 2 iconos donde puedes
                    regular el volumen de la escucha, y también el del micrófono, cuando tienes el
                    turno de micrófono.</span></p>

            <p><span>Para hablar por micrófono, se suele levantar
                    primero la mano para pedir turno. Para eso hay que pulsar el botón <i>“Raise”</i>,
                    y aparecerá una mano levantada al lado de nuestro nombre, de la lista de
                    nombres de la derecha. Una vez llega tu turno para hablar (el que está más
                    arriba en la lista de manos levantadas), hay que pulsar el botón <i>“Push to
                        talk”</i> con un doble-click del ratón. </span></p>

            <p><span>A partir de ese momento, el micrófono del
                    ordenador está funcionando y transmitiendo a la sala, y los demás lo están
                    escuchando. Para acabar el turno de micrófono, se pulsa una sola vez encima de <i>“Push
                        to talk”</i>, y se desactivará el micrófono.</span></p>

